    private void Actualizar(){
        int cliente = 0;
        String [] dui = txtDUI.getText().split("-", 2);
        String [] fecha = txtFecha.getText().split("-", 3);
        String [] telefono = txtTelefono.getText().split("-", 2);
        
        String STelefono = new StringBuilder().append(telefono[0]).append(telefono[1]).toString().trim();
        String SFecha = new StringBuilder().append(fecha[0]).append(fecha[1]).append(fecha[2]).toString().trim();
        String SDui = new StringBuilder().append(dui[0]).append(dui[1]).toString().trim();
        System.out.println(
                "N: "+ txtNombres.getText() +" A: "+ txtApellidos.getText() +
                " E: "+ txtEmail.getText() +" D: "+ 
                SDui +" F: "+ SFecha +" T: "+ STelefono 
                +" G: "+ genero +" E: "+ estadou +" Dr: "+ txtDireccion.getText() +" U: "+ txtUsuario.getText()+" N: " + nivel);
        if (!txtNombres.getText().trim().isEmpty() && !txtApellidos.getText().trim().isEmpty() &&
            !txtEmail.getText().trim().isEmpty() && !SDui.trim().isEmpty() &&
            !SFecha.trim().isEmpty() && !STelefono.trim().isEmpty() &&
            genero != 'G' && estadou > 0 &&
            !txtDireccion.getText().trim().isEmpty() && nivel != 0 &&
            !txtUsuario.getText().trim().isEmpty()){
            if (
                    (nivel != 5) 
                    || 
                    (nivel == 5 && 
                        (
                            !txtPeso.getText().trim().isEmpty() || !txtPeso.getText().equalsIgnoreCase("0.0")) 
                        && (
                            !txtAltura.getText().trim().isEmpty() || !txtAltura.getText().equalsIgnoreCase("0.0")
                        )
                    )
                ){
                Pattern pattern = Pattern.compile("^[_A-Za-z0-9-\\+]+(\\.[_A-Za-z0-9-]+)*@"
                            + "[A-Za-z0-9-]+(\\.[A-Za-z0-9]+)*(\\.[A-Za-z]{2,})$");
                Matcher mather = pattern.matcher(txtEmail.getText());

                if (mather.find() == true) {
                    String[] fechan = txtFecha.getText().split("-");
                    Integer[] fechas = new Integer[fechan.length];
                    
                    int i = 0;
                    for(String f: fechan){
                        fechas[i]=Integer.parseInt(f);
                        i++;}
                    
                    if ((fechas[0] <= 2001 && fechas[1] <= 12) &&
                        ((fechas[1] == 1 && fechas[2] <= 31) ||
                        (fechas[1] == 2 && fechas[2] <= 29) ||
                        (fechas[1] == 3 && fechas[2] <= 31) ||
                        (fechas[1] == 4 && fechas[2] <= 30) ||
                        (fechas[1] == 5 && fechas[2] <= 31) ||
                        (fechas[1] == 6 && fechas[2] <= 30) ||
                        (fechas[1] == 7 && fechas[2] <= 31) ||
                        (fechas[1] == 8 && fechas[2] <= 31) ||
                        (fechas[1] == 9 && fechas[2] <= 30) ||
                        (fechas[1] == 10 && fechas[2] <= 31) ||
                        (fechas[1] == 11 && fechas[2] <= 30) ||
                        (fechas[1] == 12 && fechas[2] <= 31))){
                        boolean a = ThirdLayer.BackEndUsuarioCliente.GetExistingDUIid(txtDUI.getText()) == 
                                ThirdLayer.BackEndUsuarioCliente.getIdcliente();
                        System.out.println(ThirdLayer.BackEndUsuarioCliente.getIdcliente());
                        System.out.println(a);
                        if (ThirdLayer.BackEndUsuarioCliente.GetExistingDUI(txtDUI.getText()) == false || 
                                ThirdLayer.BackEndUsuarioCliente.GetExistingDUIid(txtDUI.getText()) == 
                                ThirdLayer.BackEndUsuarioCliente.getIdusuario()){
                            ThirdLayer.BackEndUsuarioCliente.setNombres(txtNombres.getText().trim());
                            ThirdLayer.BackEndUsuarioCliente.setApellidos(txtApellidos.getText().trim());
                            ThirdLayer.BackEndUsuarioCliente.setUsuario(txtUsuario.getText().trim());
                            ThirdLayer.BackEndUsuarioCliente.setEmail(txtEmail.getText().trim());
                            ThirdLayer.BackEndUsuarioCliente.setDui(txtDUI.getText().trim());
                            ThirdLayer.BackEndUsuarioCliente.setNacimiento(txtFecha.getText().trim());
                            ThirdLayer.BackEndUsuarioCliente.setDireccion(txtDireccion.getText().trim());
                            ThirdLayer.BackEndUsuarioCliente.setTelefono(txtTelefono.getText().trim());
                            ThirdLayer.BackEndUsuarioCliente.setGenero(genero);
                            ThirdLayer.BackEndUsuarioCliente.setIdestado(estadou);
                            ThirdLayer.BackEndUsuarioCliente.setIdtipousuario(nivel);
                            if (nivel == 5){
                                ThirdLayer.BackEndUsuarioCliente.setPeso(Double.valueOf(txtPeso.getText().trim()));
                                ThirdLayer.BackEndUsuarioCliente.setPeso(Double.valueOf(txtAltura.getText().trim()));
                                ThirdLayer.BackEndUsuarioCliente.setIdmembresia(membresia);
                                cliente = 1;
                            }
                            if (cliente == 0){
                                if (ThirdLayer.BackEndUsuarioCliente.ActualizarUsuario())
                                    Regresar();
                                else lblError.setText(
                                "<html><p style=\"style-align:center;\">Uno de los campos tiene datos erroneos,"
                                    + " por favor verifique los datos otra vez.</p></html>"
                            );} else {
                                if (ThirdLayer.BackEndUsuarioCliente.ActualizarCliente())
                                    Regresar();
                                else lblError.setText(
                                "<html><p style=\"style-align:center;\">Uno de los campos tiene datos erroneos,"
                                    + " por favor verifique los datos otra vez.</p></html>"
                            );}
                        } else lblError.setText(
                            "<html><p style=\"style-align:center;\">El dui ingresado ya existe en nuestros registros, "
                                + "por favor verifíque si es correcto.</p></html>"
                    );} else lblError.setText(
                        "<html><p style=\"style-align:center;\">La fecha ingresada no es correcta.</p></html>"
                );} else lblError.setText(
                    "<html><p style=\"style-align:center;\">Por favor rellene el correo eléctronico para continuar.</p></html>"
            );} else lblError.setText(
                "<html><p style=\"style-align:center;\">Por favor rellene todos los campos de cliente para continuar.</p></html>"
        );} else lblError.setText(
            "<html><p style=\"style-align:center;\">Por favor rellene todos los campos de usuario para continuar. </p></html>"
    );}
    

