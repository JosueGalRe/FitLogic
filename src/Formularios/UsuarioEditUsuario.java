    private void Editar(){
        if (Tabla.getSelectedRow() != -1){
            lblError1.setText("");
            modoeditar = 1;
            ThirdLayer.BackEndUsuarioCliente.GetUserInformation((Integer) Tabla.getValueAt(Tabla.getSelectedRow(), 0));
            //Asignando valores
            txtNombres.setText(ThirdLayer.BackEndUsuarioCliente.getNombres());
            txtApellidos.setText(ThirdLayer.BackEndUsuarioCliente.getApellidos());
            txtEmail.setText(ThirdLayer.BackEndUsuarioCliente.getEmail());
            txtDireccion.setText(ThirdLayer.BackEndUsuarioCliente.getDireccion());
            txtDUI.setText(ThirdLayer.BackEndUsuarioCliente.getDui());
            txtTelefono.setText(ThirdLayer.BackEndUsuarioCliente.getTelefono());
            txtFecha.setText(ThirdLayer.BackEndUsuarioCliente.getNacimiento());
            txtUsuario.setText(ThirdLayer.BackEndUsuarioCliente.getUsuario());
            nivel = ThirdLayer.BackEndUsuarioCliente.getIdtipousuario();
            if (nivel == 5){
                txtPeso.setText(String.valueOf(ThirdLayer.BackEndUsuarioCliente.getPeso()));
                txtAltura.setText(String.valueOf(ThirdLayer.BackEndUsuarioCliente.getAltura()));
                membresia = ThirdLayer.BackEndUsuarioCliente.getIdmembresia();
            }
            genero = ThirdLayer.BackEndUsuarioCliente.getGenero();
            estadou = ThirdLayer.BackEndUsuarioCliente.getIdestado();
            System.out.println(ThirdLayer.BackEndUsuarioCliente.getIdmembresia());
            System.out.println(membresia);
            cambiarEstado(true);
            switch (membresia){
                case 1:{
                    btnPremium.setEnabled(false);
                    break;}
                case 2:{
                    btnEstandar.setEnabled(false);
                    break;}
            }
            System.out.println(nivel);
            switch (nivel){
                case 1: {
                    btnAdministrador.setEnabled(false);
                    break;
                }
                case 2:{
                    btnGerente.setEnabled(false);
                    break;
                }
                case 3:{
                    btnRecepcionista.setEnabled(false);
                    break;
                }
                case 4:{
                    btnCouch.setEnabled(false);
                    break;
                }
                case 5:{
                    btnCliente.setEnabled(false);
                    break;
                }
            }
            System.out.println(genero);
            switch (genero){
                case 'M':
                    lblGenero.setIcon(new javax.swing.ImageIcon(getClass().getResource("/Editar usuario/Barra hombre.png")));
                    break;
                case 'F':
                    lblGenero.setIcon(new javax.swing.ImageIcon(getClass().getResource("/Editar usuario/Barra mujer.png")));
                    break;
            }
            System.out.println(estadou);
            switch (estadou){
                case 1:
                    estado.setIcon(new javax.swing.ImageIcon(getClass().getResource("/Editar usuario/Barra activo.png")));
                    break;
                case 2:
                    estado.setIcon(new javax.swing.ImageIcon(getClass().getResource("/Editar usuario/Barra inactivo.png")));
                    break;
            }
            UsuarioFormularioPrincipal.AddUserMover1();
        } else lblError1.setText(
            "<html><p style=\"style-align:center;\">Por favor seleccione un usuario para modificar.</p></html>");
    }
