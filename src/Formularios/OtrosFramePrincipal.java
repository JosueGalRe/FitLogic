    //Visualizar los paneles en el panel movible
    public static void Move (javax.swing.JPanel p, int type){
        int y = 0;
        switch (type){
            case 1:
                y = 1715;
                break;
            case 2:
                y = 1960;
                break;
            case 3:
                y = 2960;
                break;
            case 4:
                y = 980;
                lblBackup.setVisible(true);
                lblLSalones.setVisible(false);
                lblLFacturacion.setVisible(false);
                lblLIndumentaria.setVisible(false);
                break;
        }
        
        panelModificable.setPreferredSize(new java.awt.Dimension(1467, y));
        PanelMovible.getVerticalScrollBar().setValue(0);
        p.setSize(1467, y);
        p.setLocation(0, 0);
        
        panelModificable.removeAll();
        panelModificable.add(p, java.awt.BorderLayout.CENTER);
        panelModificable.revalidate();
        panelModificable.repaint();
    }
