    private void btnEditarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnEditarMouseClicked
        if (Tabla.getSelectedRow() != -1){
            ThirdLayer.BackEndIndumentaria.GetIndumentaria((Integer) Tabla.getValueAt(Tabla.getSelectedRow(), 0));
            EditNombre.setText(ThirdLayer.BackEndIndumentaria.getTipo());
            EditDescripcion.setText(ThirdLayer.BackEndIndumentaria.getDescripcion());
            switch(ThirdLayer.BackEndIndumentaria.getEstado()){
                case "A":
                    EditEstado.setSelectedItem("Activo");
                    break;
                case "I":
                    EditEstado.setSelectedItem("Inactivo");
                    break;
                case "M":
                    EditEstado.setSelectedItem("Mantenimiento");
                    break;
            }
            EditCantidad.setText(String.valueOf(ThirdLayer.BackEndIndumentaria.getCantidad()));
            modoeditar = 1;
            cambiarEstado(true);
            OtrosFramePrincipal.AddUserMover1();
        }
    }//GEN-LAST:event_btnEditarMouseClicked

