    //Eliminar una indumentaria
    private void Eliminar(){
        if (Tabla.getSelectedRow() != -1 && modoeditar == 1){
            System.out.println(ThirdLayer.BackEndClases.getIdsalon());
            if (JOptionPane.showConfirmDialog(
                    null, 
                    "¿Desea eliminar la clase?", 
                    "Advertencia", 
                    JOptionPane.YES_NO_OPTION,
                    JOptionPane.QUESTION_MESSAGE) 
                    == JOptionPane.YES_OPTION){
                if (ThirdLayer.BackEndIndumentaria.EliminarIndumentaria()){
                       UsuarioSeeUsuario.RecargarTabla(Tabla, modelo, query1);
                } else JOptionPane.showMessageDialog(null, "No se ha podido eliminar el usuario.", "Error", 
                        JOptionPane.ERROR_MESSAGE);
            }
        }
    }
    //Actualizar una indumentaria 
    private void Actualizar(){
        System.out.println("N: "+EditNombre.getText()+"\nD: "+EditDescripcion.getText()+"\nC: "+EditCantidad.getText()+
                "\nE: "+EditEstado.getSelectedItem());
        if (!EditNombre.getText().trim().isEmpty() && !EditDescripcion.getText().trim().isEmpty() &&
            !EditCantidad.getText().trim().isEmpty() && EditEstado.getSelectedIndex() != -1&& 
            !("Nombre".equals(EditNombre.getText())) && !("Descripción".equals(EditDescripcion.getText())) &&
            !("Cantidad".equals(EditCantidad.getText()))){
            if((ThirdLayer.BackEndIndumentaria.GetExistingIndumentaria(EditNombre.getText()) == false || 
                ThirdLayer.BackEndIndumentaria.GetExistingIndumentariaId(EditNombre.getText()) == 
                    ThirdLayer.BackEndIndumentaria.getIdtipoequipamiento())){
                ThirdLayer.BackEndIndumentaria.setTipo(EditNombre.getText());
                ThirdLayer.BackEndIndumentaria.setDescripcion(EditDescripcion.getText());
                ThirdLayer.BackEndIndumentaria.setCantidad(Integer.valueOf(EditCantidad.getText()));
                String tipo = "";
                switch(EditEstado.getSelectedIndex()){
                    case 0:
                        tipo = "A";
                        break;
                    case 1:
                        tipo = "I";
                        break;
                    case 2:
                        tipo = "M";
                        break;
                }
                ThirdLayer.BackEndIndumentaria.setEstado(tipo);
                if (ThirdLayer.BackEndIndumentaria.ActualizarIndumentaria()){
                    LimpiarCampos("edit");
                    UsuarioSeeUsuario.RecargarTabla(Tabla, modelo, query1);
                    OtrosFramePrincipal.SeeUserMove1();
                } else lblEditError.setText(
                    "<html><p style=\"text-align:center\">Hubo un error al ingresar el equipo. "
                            + "Por favor inténtelo de nuevo.</p></html>");
            } else lblEditError.setText(
                "<html><p style=\"text-align:center\">Ya existe un equipo con ese nombre.</p></html>");
        } else lblEditError.setText(
            "<html><p style=\"text-align:center\">Uno de los campos ingresados está vacío.</p></html>");
    }
    //Agregar indumentaria
    private void Agregar(){
        if (!txtNombre.getText().trim().isEmpty() && !txtDescripcion.getText().trim().isEmpty() &&
            !txtCantidad.getText().trim().isEmpty() && cbxEstado.getSelectedIndex() != -1 && 
            !("Nombre".equals(txtNombre.getText())) && !("Descripción".equals(txtDescripcion.getText())) &&
            !("Cantidad".equals(txtCantidad.getText()))){
            if(ThirdLayer.BackEndIndumentaria.GetExistingIndumentaria(txtNombre.getText()) == false){
                ThirdLayer.BackEndIndumentaria.setTipo(txtNombre.getText());
                ThirdLayer.BackEndIndumentaria.setDescripcion(txtDescripcion.getText());
                ThirdLayer.BackEndIndumentaria.setCantidad(Integer.valueOf(txtCantidad.getText()));
                String tipo = "";
                switch(cbxEstado.getSelectedIndex()){
                    case 0:
                        tipo = "A";
                        break;
                    case 1:
                        tipo = "I";
                        break;
                    case 2:
                        tipo = "M";
                        break;
                }
                ThirdLayer.BackEndIndumentaria.setEstado(tipo);
                if (ThirdLayer.BackEndIndumentaria.IngresarIndumentaria()){
                    LimpiarCampos("add");
                    UsuarioSeeUsuario.RecargarTabla(Tabla, modelo, query1);
                } else lblError.setText(
                    "<html><p style=\"text-align:center\">Hubo un error al ingresar el equipo. "
                            + "Por favor inténtelo de nuevo.</p></html>");
            } else lblError.setText(
                "<html><p style=\"text-align:center\">Ya existe un equipo con ese nombre.</p></html>");
        } else lblError.setText(
            "<html><p style=\"text-align:center\">Uno de los campos ingresados está vacío.</p></html>");
    }

